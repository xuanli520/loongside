def check_semantic_regression_cases(taxonomy: dict) -> list[str]:
    failures = check_semantic_matcher_support(taxonomy)
    cases = semantic_regression_cases()

    for case in cases:
        case_path = case["path"]
        expected_labels = sorted(case["labels"])
        actual_labels = labels_for_path(taxonomy, case_path)

        if actual_labels == expected_labels:
            continue

        failure_message = (
            f"semantic label mismatch for {case_path}: "
            f"expected {expected_labels}, got {actual_labels}"
        )
        failures.append(failure_message)

    return failures


def render_labeler(taxonomy: dict) -> str:
    blocks: list[str] = [GENERATED_HEADER.rstrip()]
    for entry in path_labeled_entries(taxonomy):
        lines = [f'"{entry["name"]}":', "  - changed-files:", "      - any-glob-to-any-file:"]
        for path in entry["paths"]:
            lines.append(f'          - "{path}"')
        blocks.append("\n".join(lines))
    return "\n\n".join(blocks) + "\n"


def render_workflow(taxonomy: dict) -> str:
    # This renderer nests Python f-strings, GitHub expressions, and JS template
    # literals, so brace-heavy sections intentionally use doubled braces.
    managed_labels = [
        {
            "name": entry["name"],
            "color": entry["color"],
            "description": entry["description"],
        }
        for entry in label_entries(taxonomy)
    ]
    surface_label_map = {
        entry["option"]: entry["name"] for entry in taxonomy["surfaces"]
    }
    surface_label_map["Unknown / needs triage"] = None
    legacy_labels_literal = indent(
        json.dumps(taxonomy["legacy_labels"], indent=2, ensure_ascii=False),
        " " * 12,
    ).lstrip()

    managed_labels_literal = indent(
        json.dumps(managed_labels, indent=2, ensure_ascii=False),
        " " * 12,
    ).lstrip()
    surface_map_literal = indent(
        json.dumps(surface_label_map, indent=2, ensure_ascii=False),
        " " * 12,
    ).lstrip()

    script_sync = dedent(
        f"""\
        const managedLabels = {managed_labels_literal};
        const legacyLabels = {legacy_labels_literal};

        async function getLabelOrNull(name) {{
          try {{
            const {{ data }} = await github.rest.issues.getLabel({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              name
            }});
            return data;
          }} catch (error) {{
            if (error.status === 404) {{
              return null;
            }}
            throw error;
          }}
        }}

        for (const legacy of legacyLabels) {{
          const legacyLabel = await getLabelOrNull(legacy.name);
          if (!legacyLabel) {{
            continue;
          }}

          if (!legacy.replacement) {{
            await github.rest.issues.deleteLabel({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              name: legacy.name
            }});
            core.info(`Deleted legacy label ${{legacy.name}}`);
            continue;
          }}

          const replacement = managedLabels.find((label) => label.name === legacy.replacement);
          if (!replacement) {{
            throw new Error(`Missing managed replacement for legacy label ${{legacy.name}}`);
          }}

          const replacementLabel = await getLabelOrNull(replacement.name);
          if (!replacementLabel) {{
            await github.rest.issues.updateLabel({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              name: legacy.name,
              new_name: replacement.name,
              color: replacement.color,
              description: replacement.description
            }});
            core.info(`Renamed legacy label ${{legacy.name}} to ${{replacement.name}}`);
            continue;
          }}

          let page = 1;
          while (true) {{
            const {{ data: issues }} = await github.rest.issues.listForRepo({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              state: "all",
              labels: legacy.name,
              per_page: 100,
              page
            }});

            if (issues.length === 0) {{
              break;
            }}

            for (const issue of issues) {{
              const labelNames = issue.labels
                .map((label) => typeof label === "string" ? label : label.name)
                .filter(Boolean);
              const mergedLabels = labelNames.filter((name) => name !== legacy.name);
              if (!mergedLabels.includes(replacement.name)) {{
                mergedLabels.push(replacement.name);
              }}

              await github.rest.issues.setLabels({{
                owner: context.repo.owner,
                repo: context.repo.repo,
                issue_number: issue.number,
                labels: mergedLabels
              }});
            }}

            if (issues.length < 100) {{
              break;
            }}

            page += 1;
          }}

          await github.rest.issues.deleteLabel({{
            owner: context.repo.owner,
            repo: context.repo.repo,
            name: legacy.name
          }});
          core.info(`Migrated legacy label ${{legacy.name}} to ${{replacement.name}}`);
        }}

        for (const label of managedLabels) {{
          try {{
            const {{ data: current }} = await github.rest.issues.getLabel({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              name: label.name
            }});

            const currentDescription = current.description || "";
            if (
              current.color.toLowerCase() !== label.color.toLowerCase() ||
              currentDescription !== label.description
            ) {{
              await github.rest.issues.updateLabel({{
                owner: context.repo.owner,
                repo: context.repo.repo,
                name: label.name,
                new_name: label.name,
                color: label.color,
                description: label.description
              }});
              core.info(`Updated label ${{label.name}}`);
            }}
          }} catch (error) {{
            if (error.status !== 404) {{
              throw error;
            }}

            await github.rest.issues.createLabel({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              name: label.name,
              color: label.color,
              description: label.description
            }});
            core.info(`Created label ${{label.name}}`);
          }}
        }}

        core.notice(`Managed ${{managedLabels.length}} labels.`);
        """
    ).rstrip()

    script_issue = dedent(
        f"""\
        const issue = context.payload.issue;
        const surfaceLabelMap = {surface_map_literal};

        const managedSurfaceLabels = Object.values(surfaceLabelMap).filter(Boolean);
        const body = (issue.body || "").replace(/\\r\\n/g, "\\n");
        const match = body.match(/^### Surface\\s*\\n+([^\\n]+)/m);
        const selectedSurface = match ? match[1].trim() : null;
        const nextSurfaceLabel = selectedSurface ? surfaceLabelMap[selectedSurface] : null;

        const {{ data: existingLabels }} = await github.rest.issues.listLabelsOnIssue({{
          owner: context.repo.owner,
          repo: context.repo.repo,
          issue_number: issue.number
        }});

        const mergedLabels = existingLabels
          .map((label) => label.name)
          .filter((name) => !managedSurfaceLabels.includes(name));

        if (nextSurfaceLabel) {{
          mergedLabels.push(nextSurfaceLabel);
        }}

        await github.rest.issues.setLabels({{
          owner: context.repo.owner,
          repo: context.repo.repo,
          issue_number: issue.number,
          labels: mergedLabels
        }});

        if (selectedSurface) {{
          core.notice(`Resolved issue surface "${{selectedSurface}}" to ${{nextSurfaceLabel ?? "no managed surface label"}}.`);
        }} else {{
          core.notice("No Surface section found in issue body; left managed surface labels cleared.");
        }}
        """
    ).rstrip()

    content = f"""\
{GENERATED_HEADER}name: labeler

on:
  pull_request_target:
    types:
      - opened
      - reopened
      - synchronize
      - ready_for_review
      - edited
  issues:
    types:
      - opened
      - edited
      - reopened
  workflow_dispatch:

concurrency:
  group: labeler-${{{{ github.event.pull_request.number || github.event.issue.number || github.run_id }}}}
  cancel-in-progress: true

jobs:
  sync-managed-labels:
    runs-on: ubuntu-latest
    permissions:
      contents: read
      issues: write
      pull-requests: write
    steps:
      - name: Ensure managed labels exist
        uses: actions/github-script@ed597411d8f924073f98dfc5c65a23a2325f34cd
        with:
          script: |
{indent(script_sync, " " * 12)}

  label-pull-request:
    if: ${{{{ github.event_name == 'pull_request_target' }}}}
    needs: sync-managed-labels
    runs-on: ubuntu-latest
    permissions:
      contents: read
      pull-requests: write
      issues: write
    steps:
      - name: Checkout base repository
        uses: actions/checkout@de0fac2e4500dabe0009e67214ff5f5447ce83dd

      - name: Apply path labels
        uses: actions/labeler@634933edcd8ababfe52f92936142cc22ac488b1b
        with:
          repo-token: ${{{{ secrets.GITHUB_TOKEN }}}}
          configuration-path: .github/labeler.yml
          sync-labels: true

      - name: Apply size label
        uses: actions/github-script@ed597411d8f924073f98dfc5c65a23a2325f34cd
        with:
          script: |
            const issueNumber = context.payload.pull_request.number;
            const sizeLabels = ["size: XS", "size: S", "size: M", "size: L", "size: XL"];
            const files = await github.paginate(github.rest.pulls.listFiles, {{
              owner: context.repo.owner,
              repo: context.repo.repo,
              pull_number: issueNumber,
              per_page: 100
            }});

            const changedLines = files.reduce((total, file) => total + file.additions + file.deletions, 0);

            let sizeLabel = "size: XL";
            if (changedLines <= 50) {{
              sizeLabel = "size: XS";
            }} else if (changedLines <= 200) {{
              sizeLabel = "size: S";
            }} else if (changedLines <= 500) {{
              sizeLabel = "size: M";
            }} else if (changedLines <= 1000) {{
              sizeLabel = "size: L";
            }}

            const {{ data: existingLabels }} = await github.rest.issues.listLabelsOnIssue({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              issue_number: issueNumber
            }});

            const mergedLabels = [
              ...existingLabels
                .map((label) => label.name)
                .filter((name) => !sizeLabels.includes(name)),
              sizeLabel
            ];

            await github.rest.issues.setLabels({{
              owner: context.repo.owner,
              repo: context.repo.repo,
              issue_number: issueNumber,
              labels: mergedLabels
            }});

            core.notice(`Applied ${{sizeLabel}} from ${{changedLines}} changed lines.`);

  label-issue-surface:
    if: ${{{{ github.event_name == 'issues' }}}}
    needs: sync-managed-labels
    runs-on: ubuntu-latest
    permissions:
      contents: read
      issues: write
    steps:
      - name: Sync issue surface label from form
        uses: actions/github-script@ed597411d8f924073f98dfc5c65a23a2325f34cd
        with:
          script: |
{indent(script_issue, " " * 12)}
"""
    return content


def render_surface_options(taxonomy: dict) -> str:
    lines = [f"        - {entry['option']}" for entry in taxonomy["surfaces"]]
    lines.append("        - Unknown / needs triage")
    return "\n".join(lines)


