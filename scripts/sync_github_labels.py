def target_contents(repo_root: Path, taxonomy: dict) -> dict[Path, str]:
    return {
        repo_root / ".github" / "labeler.yml": render_labeler(taxonomy),
        repo_root / ".github" / "workflows" / "labeler.yml": render_workflow(taxonomy),
        repo_root / ".github" / "ISSUE_TEMPLATE" / "bug_report.yml": render_bug_template(taxonomy),
        repo_root / ".github" / "ISSUE_TEMPLATE" / "feature_request.yml": render_feature_template(taxonomy),
        repo_root / ".github" / "ISSUE_TEMPLATE" / "docs_improvement.yml": render_docs_template(taxonomy),
        repo_root / "docs" / "references" / "github-collaboration.md": render_docs_reference(taxonomy),
    }


def write_targets(targets: dict[Path, str]) -> None:
    for path, content in targets.items():
        path.write_text(content)


def check_targets(targets: dict[Path, str]) -> int:
    mismatches: list[str] = []
    for path, expected in targets.items():
        try:
            actual = path.read_text()
        except FileNotFoundError:
            mismatches.append(f"{path} (missing)")
            continue

        if actual != expected:
            mismatches.append(str(path))

    if mismatches:
        print("GitHub label taxonomy artifacts are out of date:", file=sys.stderr)
        for mismatch in mismatches:
            print(f"  - {mismatch}", file=sys.stderr)
        print("Run `python3 scripts/sync_github_labels.py --write` to refresh them.", file=sys.stderr)
        return 1
    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    mode = parser.add_mutually_exclusive_group()
    mode.add_argument("--write", action="store_true", help="write generated files")
    mode.add_argument("--check", action="store_true", help="verify generated files are up to date")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    repo_root = Path(__file__).resolve().parents[1]
    taxonomy = load_taxonomy(repo_root)
    targets = target_contents(repo_root, taxonomy)

    if args.check:
        return check_targets(targets)

    write_targets(targets)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
